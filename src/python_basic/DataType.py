### 정수형
# 양의 정수
a = 1000
print(a)

# 음의 정수
a = -7
print(a)

# 0
a = 0
print(a)

# 예제
a = 777
print(a)

a = a + 5
print(a)

a = a + 3
print(a)


### 실수형
# 양의 실수
a = 157.93
print(a)

# 음의 실수
a = -1837.2
print(a)

# 소수부가 0일때 0을 생략
a = 5.
print(a)

# 정수부가 0일때 0을 생략
a = -.7
print(a)

# 지수 표현 방식
a = 1e9
print(a)

a = 75.25e1
print(a)

a = 3954e-3
print(a)

# 예제
a = int(1e9)
print(a)


### 수 자료형의 연산
a = 7
b = 3

# 나누기
print(a / b)

# 나머지
print(a % b)

# 몫
print(a // b)

# 거듭제곱
print(a ** b)

# 제곱근
print(a ** 0.5)